/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void) {
	/* USER CODE BEGIN 1 */
	char message[50] = { '\0' };
	/* USER CODE END 1 */

	/* MCU Configuration--------------------------------------------------------*/

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* USER CODE BEGIN Init */

	/* USER CODE END Init */

	/* Configure the system clock */
	SystemClock_Config();

	/* USER CODE BEGIN SysInit */

	/* USER CODE END SysInit */

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_CAN_Init();
	MX_TIM1_Init();
	MX_TIM2_Init();
	MX_USART2_UART_Init();
	MX_ADC1_Init();
	/* USER CODE BEGIN 2 */
	HAL_CAN_Start(&hcan);

	// Activate the notification
	HAL_CAN_ActivateNotification(&hcan, CAN_IT_RX_FIFO1_MSG_PENDING);

	TxHeader.DLC = 2;  // data length
	TxHeader.IDE = CAN_ID_STD;
	TxHeader.RTR = CAN_RTR_DATA;
	TxHeader.StdId = 0x103;  // ID

	TxIVIHeader.DLC = 2;  // data length
	TxIVIHeader.IDE = CAN_ID_STD;
	TxIVIHeader.RTR = CAN_RTR_DATA;
	TxIVIHeader.StdId = 0x242;  // ID

	TxData[0] = 200;  // ms delay
	TxData[1] = 40;  // loop rep

	HAL_ADC_Start(&hadc1);  //PB1

	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2); //PA1 TIM2 CH2

	uint16_t readValue;
	int speed;
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_7, 0);
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, 0);

	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	while (1) {
		if (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_6) == GPIO_PIN_SET) { //Check if head light button pressed
			char TagCan[35] = { "Slave trigger from switch....\n\r" };
			HAL_UART_Transmit(&huart2, (uint8_t*) &TagCan, 35, 35);
			HAL_CAN_AddTxMessage(&hcan, &TxHeader, TxData, &TxMailbox);
		}
		if (datacheck) {
			// blink the LED
			for (int i = 0; i < RxData[1]; i++) {
				HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);
				HAL_Delay(RxData[0]);
			}

			datacheck = 0;

			TxData[0] = 200;   // ms Delay
			TxData[1] = 40;    // loop rep

			char TagCan[25] = { "Slave trigger....\n\r" };
			HAL_UART_Transmit(&huart2, (uint8_t*) &TagCan, 25, 25);

			HAL_CAN_AddTxMessage(&hcan, &TxHeader, TxData, &TxMailbox);

		}

		HAL_ADC_PollForConversion(&hadc1, 1000);
		//readValue = HAL_ADC_GetValue(&hadc1);
		readValue = (TIM1->CNT) >> 2;
		if (readValue < 1790) {
			HAL_GPIO_WritePin(GPIOA, GPIO_PIN_7, 0);
			HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, 1);
			speed = (readValue - 1790) * (-1) / 3;
		} else if (readValue > 2220) {
			HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, 0);
			HAL_GPIO_WritePin(GPIOA, GPIO_PIN_7, 1);
			speed = (readValue - 2220) / 3;
		} else {
			speed = 0;
		}
		__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, speed);

		if (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_10) == GPIO_PIN_RESET) {
			sprintf(message, "Switch Released, Encoder Ticks = %ld\n\r",
					((TIM1->CNT) >> 2));
			HAL_UART_Transmit(&huart2, (uint8_t*) message, sizeof(message),
					100);
			TxData[0] = 11;
			TxData[1] = ((TIM1->CNT) >> 2);
			HAL_CAN_AddTxMessage(&hcan, &TxIVIHeader, TxData, &TxMailbox);
		} else {
			sprintf(message, "Switch Pressed,  Encoder Ticks = %ld\n\r",
					((TIM1->CNT) >> 2));
			TxData[0] = 11;
			TxData[1] = ((TIM1->CNT) >> 2);
			HAL_CAN_AddTxMessage(&hcan, &TxIVIHeader, TxData, &TxMailbox);
			HAL_UART_Transmit(&huart2, (uint8_t*) message, sizeof(message),
					100);
		}
		HAL_Delay(500);
	}
	/* USER CODE END 3 */
}

