void HAL_CAN_RxFifo1MsgPendingCallback(CAN_HandleTypeDef *hcan) {
	char TagLightSensor[25] = { "Slave callback....\n\r" };
	HAL_UART_Transmit(&huart2, (uint8_t*) &TagLightSensor, 25, 25);
	HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO1, &RxHeader, RxData);
	if (RxHeader.DLC == 2) {
		datacheck = 1;
	}
}
